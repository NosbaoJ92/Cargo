// Start AbrirMenu
function AbrirMenu() {
    document.getElementById("divLinks2").style.display = "block";
    document.getElementById("btnAbrirMenuIcon").style.display = "none";
    document.getElementById("btnFecharMenuIcon").style.display = "block";
    document.getElementById("divLinks").style.display="none";
    }
// End AbrirMenu
// Start FecharMenu
function FecharMenu() {
    document.getElementById("divLinks2").style.display = "none";
    document.getElementById("btnFecharMenuIcon").style.display = "none";
    document.getElementById("btnAbrirMenuIcon").style.display = "block";
    document.getElementById("Logo_text").style.display="block";
}
// End FecharMenu